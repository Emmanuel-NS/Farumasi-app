const mkAvail = (pharmacyIdx: number, stock: "High" | "Medium" | "Low" | "Out", price: number): MedicineAvailability => {
  const pha = mockPharmacies[pharmacyIdx];
  const percentMap = { High: 85, Medium: 55, Low: 20, Out: 0 };
  return {
    pharmacyId: pha.id,
    pharmacyName: pha.name,
    inStock: stock !== "Out",
    stockLevel: stock,
    stockPercent: percentMap[stock],
    price,
    lastUpdated: new Date(Date.now() - Math.random() * 3600000 * 6).toISOString(),
    distanceKm: pha.distanceKm,
  };
};

